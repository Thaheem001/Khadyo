setInterval(() => {
    var time = new Date();
    var display = document.querySelector(".time-js");
    var display1 = document.querySelector(".date-js");
    var hours = time.getHours();
    var minute = time.getMinutes();
    var second = time.getSeconds();
    var days = time.getDay();
    var date = time.getDate();
    var month = time.getMonth();
    var year = time.getFullYear();
    var dayNight = "AM"

    if (hours > 12) {
        dayNight = "PM";
        hours -= 12;
    }
    if (hours < 10) {
        hours = "0" + hours;
    }
    if (minute < 10) {
        minute = "0" + minute;
    }
    if (second < 10) {
        second = "0" + second;
    }

    if (days == 0) {
        days = "Sunday";
    }
    if (days == 1) {
        days = "Monday";
    }
    if (days == 2) {
        days = "Tuseday";
    }
    if (days == 3) {
        days = "wednesday";
    }
    if (days == 4) {
        days = "Thursday";
    }
    if (days == 5) {
        days = "Friday";
    }
    if (days == 6) {
        days = "Saturday";
    }


    display.innerHTML = hours + ":" + minute + ":" + second + " " + dayNight;
    display1.innerHTML = days + ", " + date + "  " + month + ",  " + year;
}, 1000);